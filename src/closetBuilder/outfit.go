package closetBuilder

import (
	util "OnlineCloset/src/util"
)

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//// TYPES /////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

type Outfit struct {
	name     string
	items    []Item
	wears    int
	outfitID int

	// TODO: weather, occasion, tags?
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////CONSTRUCTORS///////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

func CreateOutfit(items []Item) Outfit {
	return Outfit{
		name:     "Unnamed Outfit",
		items:    util.RemoveCustomDuplicates(items),
		wears:    0,
		outfitID: -1,
	}
}

func CreateCopyOutfit(o Outfit) Outfit {
	newItems := make([]Item, len(o.items))
	copy(newItems, o.items)

	return Outfit{
		items:    newItems,
		wears:    0,
		outfitID: -1,
	}
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////ACCESSORS//////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

func (o Outfit) GetItems() []Item { return o.items }

func (o Outfit) GetWears() int { return o.wears }

func (o Outfit) GetID() int { return o.outfitID }

////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////MUTATORS///////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

func (o *Outfit) SetItems(items []Item) { o.items = util.RemoveCustomDuplicates(items) }

func (o *Outfit) SetWears(wears int) {
	if wears < 0 {
		return
	}
	o.wears = wears
}

func (o *Outfit) AddWear() { o.wears++ }

func (o *Outfit) SetOutfitID(outfitID int) { o.outfitID = outfitID }
